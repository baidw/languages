#!/usr/bin/perl

print "所有数字在内部的格式一样";
print "\n";
#浮点数
print 1.25;
print "\n";
print 255.000;
print "\n";
print 7.25e2;
print "\n";
print -6.25e3;
print "\n";
print -1.2E-12;
print "\n";
#整数也可以通过_分割开
print 123_456_789;
print "\n";

#非十进制整数
print 0377;
print "\n";
print 0xff;
print "\n";
print 0b1111111;
print "\n";

#数字操作
print 2+3;
print "\n";
print 5.1-2.4;
print "\n";
print 3*1.2;
print "\n";
print 123/23;
print "\n";
print 10.2/3.0;
print "\n";
